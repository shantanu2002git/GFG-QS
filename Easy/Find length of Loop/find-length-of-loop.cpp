//Function to find the length of a loop in the linked list.

int go(struct Node* slow) {
    int c = 1;
    struct Node* first = slow->next;
    while (first != slow) {
        c++;
        first = first->next;
    }
    return c;
}

int countNodesinLoop(struct Node* head) {
    struct Node* first = head, *slow = head;
    int c = 0;
    while (first != NULL && first->next != NULL) {
        first = first->next->next;
        slow = slow->next;
        if (first == slow) {
            return go(slow);
        }
    }
    return c;
}